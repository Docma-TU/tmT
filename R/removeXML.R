        for(i in 1:nrow(entityList)){
            cat(entityList[i,"Character"], entityList[i,"htmlName"], "\n")
            x <- gsub(pattern=entityList[i,"htmlName"], replacement=entityList[i,"Character"],x)}}
    if(delete) x <- gsub(pattern="&[^;]*;", replacement="", x)
    x <- trimws(x)
    return(x)
}

